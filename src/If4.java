//write multiple conditions
public class If4 {
    public static void main(String[] args) {
        //positive > 0, negative < 0, 0 -> the number is 0
        int x = -10;
        
        if (x > 0){
            System.out.println("positive");
        }else if (x < 0){
            System.out.println("negative");
        }else{
            System.out.println("Number is zero");
        }
    }
 
}
